#include <stdio.h>
int main()
{
    for( int i=0; i<5; i++){ ///迴圈基本型: 電腦,精簡
        printf("電腦從0開始數, i:%d\n", i);
    }
    for( int i=1; i<=5; i++){ ///迴圈進階型: 電腦,精簡
        printf("人類從1開始數多一點點, i:%d\n", i);
    }
    for( int i=5; i>0; i--){ ///迴圈倒過來型: 電腦,精簡
        printf("倒過來的迴圈, i:%d\n", i);
    }
}
